            <h3>Bangladesh National Cadet Corps</h3>
                <h2>History of BNCC</h2>
                    <h4>
                        <b>UCC IN 1920-</b> British govt. formed University Cadet Corps (UCC) in various Universities.<br><br>
                        <b>UTC IN 1923-</b> UCC was renamed as University Training Corps (UTC).<br><br>
                        <b>12 DHAKA COMPANY IN 1928-</b> A UTC Company was raised of Dhaka University as 12 Dhaka Company.<br><br>
                        <b>UOTC IN 1950-</b>In 1950 UTC was renamed as UOTC or University Officer Training Corps.<br><br>
                        <b>JCC IN 1966-</b> Program was extended to few colleges and named as junior Cadet Corps or JCC.<br><br>
                        <b>BCC-</b>Cadet Corps program was further extended to schools as Bangladesh Cadet Corps (BCC).<br><br>
                        <b>BNCC IN 1979-</b> Finally UTC, UOTC, JCC and BCC were merged together and formed as Bangladesh National Cadet Corps.
                    </h4>
                    <br><br>
